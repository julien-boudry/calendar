test('jd to ymd reciprocity', function (): void {
    $calendar = new JewishCalendar;

    for ($jd = $calendar->jdStart(); $jd < min(2457755, $calendar->jdEnd()); $jd += 79) {
        [$y, $m, $d] = $calendar->jdToYmd($jd);
        expect($calendar->ymdToJd($y, $m, $d))->toBe($jd);
    }
});
